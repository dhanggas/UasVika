    private void kosong() {
        txtPajak.setText("0");
        txtDiskon.setText("0");
        txtKembali.setText("0");
        txtHarga.setText("");
        txtBayar.setText("");
        txtHarga.setFocusable(true);

    }

    public void tampilkeJTable() {
        try {
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            for (Bayaran m : this.controler.ambilseumaData()) {
                Object[] databaris = {m.getHarga(), m.getBayar(), m.getPajak(), m.getDiskon(), m.getKembali()};
                this.model.addRow(databaris);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

