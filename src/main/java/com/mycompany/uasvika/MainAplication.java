    private void txtBayarCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtBayarCaretUpdate

        try {
            harga = Integer.valueOf(txtHarga.getText());
            pajak = (int) (0.01 * harga);
            txtPajak.setText(String.valueOf(pajak));
            if (harga >= 100000) {
                diskon = (int) (0.01 * harga);
            } else if (harga >= 50000 && harga <= 99900) {
                diskon = (int) (0.001 * harga);
            } else {
                diskon = 0;
            }
            txtDiskon.setText(String.valueOf(diskon));

            bayar = Integer.valueOf(txtBayar.getText());
            kembali = bayar - (harga + pajak - diskon);
            txtKembali.setText(String.valueOf(kembali));

        } catch (Exception e) {
        }
    }//GEN-LAST:event_txtBayarCaretUpdate

