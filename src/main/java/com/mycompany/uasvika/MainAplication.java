    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        try {
            Bayaran m = new Bayaran();
            m.setHarga(Integer.valueOf(txtHarga.getText()));
            m.setBayar(Integer.valueOf(txtBayar.getText()));
            m.setPajak(Integer.valueOf(txtPajak.getText()));
            m.setDiskon(Integer.valueOf(txtDiskon.getText()));
            m.setKembali(Integer.valueOf(txtKembali.getText()));

            controler.simpanData(m);
        } catch (SQLException ex) {
            Logger.getLogger(MainAplication.class.getName()).log(Level.SEVERE, null, ex);
        }
        tampilkeJTable();
        kosong();
        JOptionPane.showMessageDialog(this, "Data Berhasil Di Simpan", getTitle(), JOptionPane.YES_OPTION);

    }//GEN-LAST:event_btnsimpanActionPerformed

